    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        String name = jTextField1.getText();
        String details = jTextArea1.getText();
        String dates = Showdate.getText();

        if (name == "" || dates == "")  {
            jLabel9.setText("Please enter required fields");
        } else {
            try {

                ZoneId z = ZoneId.of("Antarctica/McMurdo");
                LocalDate today = LocalDate.now();

                DateTimeFormatter f = DateTimeFormatter.ofPattern("uuuu-MM-dd");
                LocalDate ld = LocalDate.parse(dates, f);

                if (ld.isBefore(today)) {
                    jLabel9.setText("Date is Before Today");
                } else {
                    valDate(dates);
                    controller.NewTask(name, details, dates);
                    this.dispose();
                    new ListTask().setVisible(true);

                }
            } catch (Exception e) {
                jLabel9.setText("Invalid Date");
            }

        } 
    }//GEN-LAST:event_jButton2ActionPerformed

