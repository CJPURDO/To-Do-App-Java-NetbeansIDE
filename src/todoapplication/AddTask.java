    /**
     * Validate date
     * @param idate
     */
    public void valDate(String idate) {
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("uuuu-MM-dd", Locale.UK).withResolverStyle(ResolverStyle.STRICT);

        try {
            formatter.parse(idate);
        } catch (Exception e) {
            jLabel9.setText("Invalid Date");
        }
    }
    
    

